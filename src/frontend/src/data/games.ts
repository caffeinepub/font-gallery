function generateGames(count: number): GameTemplate[] {
  const generated: GameTemplate[] = [];
  let seed = 42;
  function rand(max: number) {
    seed = (seed * 1664525 + 1013904223) & 0xffffffff;
    return Math.abs(seed) % max;
  }
  for (let i = 0; i < count; i++) {
    const cat = CATEGORY_POOL[rand(CATEGORY_POOL.length)];
    const prefix = GAME_NAME_PREFIXES[rand(GAME_NAME_PREFIXES.length)];
    const suffixes = GAME_NAME_SUFFIXES[cat];
    const suffix = suffixes[rand(suffixes.length)];
    const name = `${prefix} ${suffix} ${i + 1}`;
    const iconStyle = ICON_POOL[rand(ICON_POOL.length)];
    const tipSet = GENERIC_TIPS[rand(GENERIC_TIPS.length)];
    const numFeatures = 2 + rand(3);
    const featureStart = rand(GENERIC_MOD_FEATURES.length - numFeatures);
    const features = GENERIC_MOD_FEATURES.slice(
      featureStart,
      featureStart + numFeatures,
    );
    const sizeMB = 50 + rand(3000);
    const downloads = 1 + rand(200);
    const rating = 3.5 + rand(15) / 10;
    const version = `${1 + rand(5)}.${rand(20)}.${rand(99)}`;
    generated.push({
      name,
      category: cat,
      description: `Enjoy an exciting ${cat.toLowerCase()} experience with stunning graphics and addictive gameplay. Mod features include ${features.join(", ")} and much more.`,
      sizeMB,
      downloads,
      version,
      modFeatures: features,
      rating: Math.min(5, Math.round(rating * 10) / 10),
      iconBg: iconStyle.bg,
      iconColor: "#fff",
      icon: iconStyle.icon,
      tips: tipSet,
    });
  }
  return generated;
}

